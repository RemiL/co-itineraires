package ligne;

/**
 * class Horaire représentant un horaire doté d'heures et de minutes
 * @author Marie
 *
 */
class Horaire
{
	int h, m;
	
	/**
	 * constructeur Horaire
	 * @param h les heures
	 * @param m les minutes
	 */
	public Horaire(int h, int m)
	{
		this.h = h;
		this.m = m;
	}
}
